import mongoose from "mongoose";

// Creating UserSchema with Name, Email, Password
const userSchema = mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

// Exporting UserSchema
export default mongoose.model("user", userSchema);
